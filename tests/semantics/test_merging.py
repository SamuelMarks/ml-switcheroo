def test_infer_tier_from_priority():
  assert infer_tier_from_priority(10) == SemanticTier.ARRAY_API
  assert infer_tier_from_priority(20) == SemanticTier.NEURAL
  assert infer_tier_from_priority(99) == SemanticTier.EXTRAS
  assert infer_tier_from_priority(0) == SemanticTier.EXTRAS


def test_merge_frameworks():
  master = {}

  # Empty to start, adding brand new framework
  merge_frameworks(master, {"torch": {"alias": {"x": "y"}, "traits": {"foo": "bar"}}})
  assert "torch" in master
  assert master["torch"]["alias"]["x"] == "y"

  # Merge into existing, updating alias
  merge_frameworks(master, {"torch": {"alias": {"a": "b"}, "other": 1}})
  assert master["torch"]["alias"] == {"x": "y", "a": "b"}
  assert master["torch"]["other"] == 1

  # Merge into existing, overwriting traits
  merge_frameworks(master, {"torch": {"traits": {"new": "val"}}})
  assert master["torch"]["traits"] == {"new": "val"}

  # Merge into existing, generic update
  merge_frameworks(master, {"torch": {"new_key": "new_val"}})
  assert master["torch"]["new_key"] == "new_val"


def test_merge_patterns(capsys):
  master = [PatternDef(name="Existing", sequence=["a"], replace_with="b")]

  # Add a valid new pattern
  merge_patterns(master, [{"name": "NewPat", "sequence": ["x"], "replace_with": "y"}])
  assert len(master) == 2
  assert master[1].name == "NewPat"

  # Add a duplicate pattern
  merge_patterns(master, [{"name": "Existing", "sequence": ["a"], "replace_with": "b"}])
  assert len(master) == 2

  # Add an invalid pattern
  merge_patterns(master, [{"name": "BadPat"}])  # Missing fields
  assert len(master) == 2
  captured = capsys.readouterr()
  assert "Invalid pattern definition:" in captured.out


