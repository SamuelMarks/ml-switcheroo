def test_latex_dsl_backend_unranked_node():
  from ml_switcheroo.compiler.backends.extras import TikzBackend as LatexBackend
  from ml_switcheroo.core.graph import LogicalGraph, LogicalNode, LogicalEdge
  from ml_switcheroo.semantics.manager import SemanticsManager

  backend = LatexBackend(SemanticsManager())
  # Node not connected to input so it doesn't get ranked in BFS
  graph = LogicalGraph(
    nodes=[LogicalNode("in", "Input"), LogicalNode("a", "Conv2d"), LogicalNode("b", "Conv2d")],
    edges=[LogicalEdge("a", "b"), LogicalEdge("b", "a")],
  )
  # line 453-454: max_rank += 1; ranks[n.id] = max_rank
  code = backend.compile(graph)
  assert "Conv2d" in code


def test_latex_backend_visited_ops():
  from ml_switcheroo.compiler.backends.extras import LatexBackend
  from ml_switcheroo.core.graph import LogicalGraph, LogicalNode, LogicalEdge
  from ml_switcheroo.semantics.manager import SemanticsManager

  backend = LatexBackend(SemanticsManager())
  graph = LogicalGraph(
    nodes=[LogicalNode("i1", "Input"), LogicalNode("i2", "Input"), LogicalNode("op", "add")],
    edges=[LogicalEdge("i1", "op"), LogicalEdge("i2", "op")],
  )
  code = backend.compile(graph)

  graph2 = LogicalGraph(
    nodes=[
      LogicalNode("in", "Input"),
      LogicalNode("func_add_id", "func_add", {"arg_2": "val", "kw": "2"}),
      LogicalNode("func_dot", "torch.add", {}),
    ],
    edges=[LogicalEdge("in", "func_add_id"), LogicalEdge("func_add_id", "func_dot")],
  )
  code2 = backend.compile(graph2)
  assert "func_add" not in code2.replace("func_add_id", "replaced")
  assert "Add" in code2

  # Test HtmlBackend for dot handling
  from ml_switcheroo.compiler.backends.extras import HtmlBackend

  backend3 = HtmlBackend(SemanticsManager())
  code3 = backend3.compile(graph2)
  assert "Add" in code3


def test_html_backend_visited_ops():
  from ml_switcheroo.compiler.backends.extras import HtmlBackend
  from ml_switcheroo.core.graph import LogicalGraph, LogicalNode, LogicalEdge
  from ml_switcheroo.semantics.manager import SemanticsManager

  backend = HtmlBackend(SemanticsManager())
  # line 647: target_id in visited_ops
  # Force two edges to same target so it's visited twice
  graph = LogicalGraph(
    nodes=[LogicalNode("i1", "Input"), LogicalNode("i2", "Input"), LogicalNode("op", "add")],
    edges=[LogicalEdge("i1", "op"), LogicalEdge("i2", "op")],
  )
  code = backend.compile(graph)
  assert "add" in code

  # 660-674: metadata handling, func_ prefix, etc.
  graph2 = LogicalGraph(
    nodes=[
      LogicalNode("in", "Input"),
      LogicalNode("func_add_id", "func_add", {"arg_2": "val", "kw": "2"}),
      LogicalNode("func_dot", "torch.add", {}),
    ],
    edges=[LogicalEdge("in", "func_add_id"), LogicalEdge("func_add_id", "func_dot")],
  )
  code2 = backend.compile(graph2)
  assert "func_add" not in code2.replace("func_add_id", "replaced")
  assert "Add" in code2

  # Test HtmlBackend for dot handling
  from ml_switcheroo.compiler.backends.extras import HtmlBackend

  backend3 = HtmlBackend(SemanticsManager())
  code3 = backend3.compile(graph2)
  assert "Add" in code3  # should be "Add"
  assert "val" in code2
  assert "kw=2" in code2
