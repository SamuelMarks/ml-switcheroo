def test_html_backend_is_stateful():
  backend = HtmlBackend()
  assert backend._is_stateful(LogicalNode("n1", "Input")) is False
  assert backend._is_stateful(LogicalNode("n2", "func_add")) is False
  assert backend._is_stateful(LogicalNode("func_n3", "Linear")) is False
  assert backend._is_stateful(LogicalNode("n4", "Linear")) is True
  assert backend._is_stateful(LogicalNode("n5", "linear")) is False
  # Empty kind
  assert backend._is_stateful(LogicalNode("n6", "")) is False


